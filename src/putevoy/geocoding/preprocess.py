"""Препроцессинг адреса для геокодеров — раскрытие сокращений."""
from __future__ import annotations

import re

# Распространённые сокращения в русских адресах
ABBREVIATIONS = [
    (r"\bпр-?т\.?", "проспект"),
    (r"\bпр\.", "проспект"),
    (r"\bул\.", "улица"),
    (r"\bпер\.", "переулок"),
    (r"\bш\.", "шоссе"),
    (r"\bнаб\.", "набережная"),
    (r"\bпл\.", "площадь"),
    (r"\bбул\.", "бульвар"),
    (r"\bб-р\b", "бульвар"),
    (r"\bмкр\.", "микрорайон"),
    (r"\bд\.\s*(?=\d)", "дом "),
    (r"\bкорп\.", "корпус"),
    (r"\bстр\.", "строение"),
    (r"\bобл\.", "область"),
    (r"\bг\.\s*(?=[А-ЯA-Z])", "город "),
    (r"\bр-н\b", "район"),
]


def expand_abbreviations(address: str) -> str:
    """Раскрыть сокращения: 'Невский пр. 1' → 'Невский проспект 1'."""
    s = address
    for pattern, replacement in ABBREVIATIONS:
        s = re.sub(pattern, replacement, s, flags=re.IGNORECASE)
    # Чистим двойные пробелы
    s = re.sub(r"\s+", " ", s).strip()
    return s


def compact_for_nominatim(address: str) -> str:
    """Сжать адрес в компактный формат, понятный OpenStreetMap Nominatim.

    Nominatim/OSM в РФ ожидают:
      - «Репищева 10к3» вместо «улица Репищева, дом 10, корпус 3»
      - убираются слова «улица», «дом», запятые между ними
      - «корпус N» / «корп. N» / «к. N» → «кN» без пробела
      - «строение N» / «стр. N» → «стрN»
    """
    s = address
    # Убираем разделительные слова перед именами и номерами
    s = re.sub(r"\bулица\b\s*", "", s, flags=re.IGNORECASE)
    s = re.sub(r"\bул\.\s*", "", s, flags=re.IGNORECASE)
    s = re.sub(r"\bдом\b\s*(?=\d)", "", s, flags=re.IGNORECASE)
    s = re.sub(r"\bд\.\s*(?=\d)", "", s, flags=re.IGNORECASE)
    # Корпус: «корпус N» / «корп. N» / «к. N» / «к N» → «кN»
    s = re.sub(r"\bкорпус\s*(\d+)", r"к\1", s, flags=re.IGNORECASE)
    s = re.sub(r"\bкорп\.\s*(\d+)", r"к\1", s, flags=re.IGNORECASE)
    s = re.sub(r"\bк\.\s*(\d+)", r"к\1", s, flags=re.IGNORECASE)
    # Строение
    s = re.sub(r"\bстроение\s*(\d+)", r"стр\1", s, flags=re.IGNORECASE)
    s = re.sub(r"\bстр\.\s*(\d+)", r"стр\1", s, flags=re.IGNORECASE)
    # Чистим запятые-дубликаты и пробелы
    s = re.sub(r",\s*,", ",", s)
    s = re.sub(r"\s+", " ", s).strip(", ")
    # Запятая между названием улицы и номером дома → пробел («Репищева, 10» → «Репищева 10»).
    # Действует только когда после запятой идёт исключительно номер (с возможным к/стр).
    s = re.sub(r",\s*(\d+(?:\s*[кстр]+\d+)?)\s*(,|$)", r" \1\2", s)
    # Склейка номера и корпуса/строения: «10, к3» → «10к3», «10 к3» → «10к3»
    s = re.sub(r"(\d+)[\s,]+к(\d+)", r"\1к\2", s)
    s = re.sub(r"(\d+)[\s,]+стр(\d+)", r"\1стр\2", s)
    # Финальная чистка от возможных хвостов
    s = re.sub(r",\s*,", ",", s)
    s = re.sub(r"\s+", " ", s).strip(", ")
    return s
